TRANSITION = r'^(?:[\s]*)([\w]+)(?:[\s,]+)([\w]+)(?:[\s,]+)([\w]+)(?:[\s]*)$'
ASSIGNMENT = r'^(?:[\s]*)([\w]+)(?:(?:[\s]*)(=)((?:[\s]*)(.+))?)?(?:[\s]*)$'
FAIL_STATE = r'^(?:[\s]*)(?:fail|FAIL)(?:[\s]*)\((?:[\s]*)(?:\'|\")(.*)(?:\'|\")(?:[\s,]*)([0-9]{4})\)(?:[\s]*)$'
